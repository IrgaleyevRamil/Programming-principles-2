# BOOLEAN VALUES
# Example 1
a = True
b = False
# prints boolean variables
print(a)
print(b)

# Example 2
# prints boolean literals
print(True)
print(False)

# Example 3
x = 5
# comparison returns a boolean value
print(x > 3)

# Example 4
is_student = True
# prints student status
print(is_student)

# Example 5
flag = False
# prints flag value
print(flag)


# BOOLEANS AS COMPARISON RESULTS
# Example 1
# checks if 10 is greater than 5
print(10 > 5)

# Example 2
# checks equality
print(3 == 3)

# Example 3
# checks inequality
print(4 != 7)

# Example 4
# checks less than or equal
print(8 <= 5)

# Example 5
# checks greater than or equal
print(6 >= 6)

# Example 6
a = 200
b = 33
# compares two numbers using if else
if b > a:
    print("b is greater than a")
else:
    print("b is not greater than a")


# BOOLEAN OPERATORS
# Example 1
# logical AND with two True values
print(True and True)

# Example 2
# logical AND returns False
print(True and False)

# Example 3
# logical OR returns True
print(True or False)

# Example 4
# logical OR with two False values
print(False or False)

# Example 5
# logical NOT inverts the value
print(not True)

# Example 6
x = "Hello"
y = 15
# converts values to boolean
print(bool(x))
print(bool(y))
